local stardict = require("stardict")

T.add("stardict: parse_ifo", function()
    local info = assert(stardict.parse_ifo(T.fixture_dir .. "/dz/fixture.ifo"))
    T.eq(info.bookname, "Fixture fixture h")
    T.eq(info.wordcount, "6")
    T.eq(info.synwordcount, "4")
    T.eq(info.sametypesequence, "h")
    local bad = stardict.parse_ifo(T.fixture_dir .. "/dz/fixture.idx")
    T.eq(bad, nil, "idx is not an ifo")
end)

T.add("stardict: parse_ifo handles long description before other fields", function()
    local path = os.tmpname()
    local f = assert(io.open(path, "wb"))
    f:write("StarDict's dict ifo file\n")
    f:write("version=2.4.2\n")
    f:write("description=" .. string.rep("x", 4000) .. "\n")
    f:write("bookname=Long Desc Dict\n")
    f:write("wordcount=1\n")
    f:write("sametypesequence=h\n")
    f:close()
    local info = assert(stardict.parse_ifo(path))
    T.eq(info.bookname, "Long Desc Dict")
    T.eq(info.sametypesequence, "h")
    os.remove(path)
end)

T.add("stardict: cmp is stardict_strcmp", function()
    T.eq(stardict.cmp("a", "a"), 0)
    T.eq(stardict.cmp("a", "b"), -1)
    T.eq(stardict.cmp("b", "a"), 1)
    -- caseless primary: 'B' sorts with 'b', before 'c'
    T.eq(stardict.cmp("Book", "car"), -1)
    T.eq(stardict.cmp("car", "Book"), 1)
    -- strcmp tiebreak on caseless equality: "Book" < "book" ('B'=66 < 'b'=98)
    T.eq(stardict.cmp("Book", "book"), -1)
    T.eq(stardict.cmp("book", "Book"), 1)
    T.eq(stardict.cmp("book", "book"), 0)
    -- prefix ordering
    T.eq(stardict.cmp("book", "books"), -1)
    T.eq(stardict.cmp("books", "book"), 1)
    -- high bytes compare unsigned (UTF-8 Cyrillic after ASCII)
    T.eq(stardict.cmp("zebra", "яблуко"), -1)
    T.eq(stardict.cmp("яблуко", "яблуко"), 0)
end)

T.add("stardict: open reads metadata and support flags", function()
    local d = assert(stardict.open(T.fixture_dir .. "/dz/fixture.ifo", "/tmp"))
    T.eq(d.bookname, "Fixture fixture h")
    T.eq(d.wordcount, 6)
    T.eq(d.synwordcount, 4)
    T.eq(d.supported, true)
    local p = assert(stardict.open(T.fixture_dir .. "/plain/fixture.ifo", "/tmp"))
    T.eq(p.synwordcount, 0)
    T.eq(p.supported, true)
end)

T.add("stardict: scan_offsets finds entry boundaries", function()
    -- fixture idx: 6 entries, each word\0 + 8 bytes
    local offs, err = stardict.scan_offsets(T.fixture_dir .. "/dz/fixture.idx", 8, 6)
    assert(offs, err)
    T.eq(offs[0], 0)
    local size = stardict._file_stat(T.fixture_dir .. "/dz/fixture.idx")
    T.eq(offs[6], size, "sentinel must equal idx file size")
    -- syn: 4 entries, word\0 + 4 bytes
    local soffs = assert(stardict.scan_offsets(T.fixture_dir .. "/dz/fixture.syn", 4, 4))
    local ssize = stardict._file_stat(T.fixture_dir .. "/dz/fixture.syn")
    T.eq(soffs[4], ssize)
    -- wrong expected count must fail
    local bad, berr = stardict.scan_offsets(T.fixture_dir .. "/dz/fixture.idx", 8, 5)
    T.eq(bad, nil)
    assert(berr:match("mismatch"), "expected mismatch error, got: " .. tostring(berr))
end)

T.add("stardict: ensure_index builds then reloads sidecar", function()
    os.remove(T.fixture_dir .. "/dz/fixture.fdx")
    local d = assert(stardict.open(T.fixture_dir .. "/dz/fixture.ifo", "/tmp"))
    assert(d:ensure_index())
    T.eq(d.n_idx, 6)
    T.eq(d.n_syn, 4)
    assert(stardict._file_stat(d:cache_path()), "sidecar file must exist")
    -- second open must LOAD (not rebuild): corrupt detection sanity —
    -- loading again gives identical arrays
    local d2 = assert(stardict.open(T.fixture_dir .. "/dz/fixture.ifo", "/tmp"))
    assert(d2:ensure_index())
    for i = 0, 6 do T.eq(d2.idx_off[i], d.idx_off[i]) end
    for i = 0, 4 do T.eq(d2.syn_off[i], d.syn_off[i]) end
end)

T.add("stardict: stale/corrupt sidecar is rebuilt", function()
    local d = assert(stardict.open(T.fixture_dir .. "/dz/fixture.ifo", "/tmp"))
    assert(d:ensure_index())
    local cp = d:cache_path()
    local f = assert(io.open(cp, "r+b"))
    f:write("JUNK")  -- clobber magic
    f:close()
    local d3 = assert(stardict.open(T.fixture_dir .. "/dz/fixture.ifo", "/tmp"))
    assert(d3:ensure_index(), "must rebuild after corruption")
    T.eq(d3.n_idx, 6)
    local f2 = assert(io.open(cp, "rb"))
    T.eq(f2:read(4), "FDX1", "magic restored by rebuild")
    f2:close()
end)

local function open_dz()
    local d = assert(stardict.open(T.fixture_dir .. "/dz/fixture.ifo", "/tmp"))
    assert(d:ensure_index())
    return d
end

T.add("stardict: exact idx lookup, case-sensitive", function()
    local d = open_dz()
    local r = d:lookup("book")
    T.eq(#r, 2, "two lowercase 'book' entries")
    T.eq(r[1].word, "book")
    T.eq(r[1].definition, "\n<b>book</b>: bound pages, distinct from the capitalized entry.")
    T.eq(r[2].definition, "\n<b>book</b> (2): a second entry with the same headword.")
    local rc = d:lookup("Book")
    T.eq(#rc, 1, "capitalized query matches only capitalized entry")
    T.eq(rc[1].word, "Book")
    T.eq(#d:lookup("BOOK"), 0, "no exact entry for BOOK")
    T.eq(#d:lookup("nosuchword"), 0)
end)

T.add("stardict: syn lookup resolves to idx entries", function()
    local d = open_dz()
    local r = d:lookup("apples")
    T.eq(#r, 1)
    T.eq(r[1].word, "apple", "result word is the idx headword, not the syn form")
    T.eq(r[1].definition, "\n<b>apple</b>: a fruit.")
    local u = d:lookup("яблуками")
    T.eq(#u, 1)
    T.eq(u[1].word, "яблуко")
    -- syn 'зебра' (Cyrillic) resolves to Latin 'zebra'
    T.eq(d:lookup("зебра")[1].word, "zebra")
end)

T.add("stardict: syn+idx dedup and ordering", function()
    local d = open_dz()
    -- 'books' resolves via syn to one 'book' entry; direct idx query must not dup
    local r = d:lookup("books")
    T.eq(#r, 1)
end)

T.add("stardict: xdxf definition conversion", function()
    local d = assert(stardict.open(T.fixture_dir .. "/plain/fixture.ifo", "/tmp"))
    assert(d:ensure_index())
    local r = d:lookup("cat")
    T.eq(#r, 1)
    -- '<k>cat</k>' removed, '<tr>'->'[', '</tr>'->']', '&amp;'->'&', '<b>' tags dropped
    T.eq(r[1].definition, "\nkit[kaet] & feline")
    local r2 = d:lookup("dog")
    T.eq(r2[1].definition, "\npes <canine>")
end)
